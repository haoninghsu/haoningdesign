function buildOneCard(child, div) {
  let a = document.createElement("a");
  a.innerHTML = child;

  div.appendChild(a);
}

function buildAllCards(currentArr) {
  let div = document.getElementById("cards");
  div.innerHTML = "";
  for (i = 0; i < arr.length; i++) {
    buildOneCard(currentArr[i][2], div);
  }
}

var searchInput = document.getElementById("animal-filter");
var colorDropDown = document.getElementById("color-select");
var fluffDropDown = document.getElementById("fluff-select");
var habitatDropDown = document.getElementById("habitat-select");
//compare the input with the animal list
function isAnimalFound(animal) {
  //Get the input
  var userInput = searchInput.value;
  console.log(animal[0]);
  console.log(animal[0].includes(userInput), userInput);
  if (animal[0].includes(userInput)) {
    return true;
  } else {
    return false;
  }
}

function isAnimalColor(animal) {
  var selectedColor = colorDropDown.value;
  if (animal[1] === selectedColor) {
    return true;
  } else {
    return false;
  }
}

function isAnimalFluff(animal) {
  var selectedFluff = fluffDropDown.value;
  if (animal[3] === selectedFluff) {
    return true;
  } else {
    return false;
  }
}

function isAnimalHabitat(animal) {
  var selectedColor = habitatDropDown.value;
  if (animal[4] === selectedColor) {
    return true;
  } else {
    return false;
  }
}

//listen when user types in the filed
searchInput.addEventListener("input", function () {
  //filter animals
  var filtered_animals = arr.filter(isAnimalFound);
  /*console.log(arr); */
  console.log(filtered_animals);
  //Update and display the list
  buildAllCards(filtered_animals);
});

colorDropDown.addEventListener("change", function () {
  var filtered_animals = arr.filter(isAnimalColor);
  buildAllCards(filtered_animals);
});

fluffDropDown.addEventListener("change", function () {
  var filtered_animals = arr.filter(isAnimalFluff);
  buildAllCards(filtered_animals);
});

habitatDropDown.addEventListener("change", function () {
  var filtered_animals = arr.filter(isAnimalHabitat);
  buildAllCards(filtered_animals);
});
